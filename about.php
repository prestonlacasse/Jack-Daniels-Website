                    <li><h2 style="color: #ffdca7">1866 - 1904 </h2></li>
                    <li><p>Jack Daniel’s Distillery was established in 1866, and was the first registered distillery in the U.S. born Jasper Newton Daniel, its namesake is the 
                    mastermind behind the Old No. 7 taste that is so popular. By 1904, Jack’s infamous whiskey had earned a gold medal at the World’s Fair. The secret to Jack’s award-winning 
                    flavor is limestone spring water, which is the resource used today at the Lynchburg, TN location. His original recipe, which remains unchanged, also includes 
                    just the right amount of corn, rye, and barley.</p></li>
                    <li><img src="images/Jack-Daniel-photo.jpg" height="300px" width="235px"></li>
                    <li></li><p>It all started when Jack ran away from home after his mother and father had both died. Prior to his death, Jack’s father remarried, but Jack despised his stepmother and was taken in by a local preacher named Dan Call. Call began teaching Jack about distilling, and in 1875 Jack founded a registered distilling business with Call using money from his late father’s estate. Shortly thereafter, Call quit the business due to religious reasons, and Jack took over the ownership.</p></li>
                    <li><h2 style="color: #ffdca7">1907 - 1910 </h2></li>
                    <li><p>In 1884, Jack Daniel purchased the land where the distillery is located today. He ran it until 1907, when he gave the distillery to his nephew, Lemuel “Lew” Motlow, since he never married or had children. Motlow operated the distillery for 40 years. In 1910, he challenged the law in a case that went to the Tennessee Supreme Court. Since a statewide prohibition was passed in 1910, the legal distillation of Jack Daniel’s in Tennessee was not allowed.</p></li>
                    <li><h2 style="color: #ffdca7">1911 - 1947 </h2></li>
                    <li><p>The company then moved production to St. Louis, Missouri, and Birmingham, Alabama, but was not able to achieve the quality of whiskey that was produced in Tennessee. Both Missouri and Alabama were affected by similar prohibitions, and after several years of repeals and changes to the law, the distillery was finally up and running for business in 1947.</p></li>
                    <li><img src="images/Home_Share.jpg" height="235px" width="350px"></li>
                    <li><p>While it is now legal to distill whiskey in Tennessee, the distillery is actually located in a dry county. This means that while the product can legally be produced, it cannot legally be sold. However, there is one exception, which is that Jack Daniel’s can sell one commemorative product. You can purchase Gentleman Jack, Jack Daniel’s Single Barrel, or Old No. 7 at the distillery’s bottle shop. Free tours are offered daily at the distillery from 9:00 a.m. to 4:30 p.m., except on Thanksgiving, Christmas Eve, Christmas Day, and New Year’s Day. After each tour, guests are served a small cup of lemonade, since the dry country prohibits any alcohol sales.</p></li>
